// Add a add function
pub fn add(a: i32, b: i32) -> i32 {
    a + b
}

// Add subtract function
pub fn subtract(a: i32, b: i32) -> i32 {
    a - b
}

// Add multiply function
pub fn multiply(a: i32, b: i32) -> i32 {
    a * b
}

// Add divide function
pub fn divide(a: i32, b: i32) -> i32 {
    a / b
}
